package BankAccount;
public class Account {
private String name;
private String address;
private String dateOfBirth;
private int accountNo;
private String accountType;
private double accountBalance;
private boolean accountStatus;
private double accountDeposit;
private double accountWithdrawel;

public String getName() {
return name;
}
public void setName(String name) {
this.name=name;
}
public String getAddress() {
return address;
}
public void setAddress(String address) {
this.address=address;
}
public String getdateOfBirth() {
return dateOfBirth;
}
public void setdateOfBirth(String dateOfBirth) {
this.dateOfBirth=dateOfBirth;
}
public int getaccountNo() {
return accountNo;
}
public void setaccountNo(int accountNo) {
this.accountNo=accountNo;
}
public String getaccountType() {
return accountType;
}
public void setaccountType(String accountType) {
this.accountType=accountType;}
public double getaccountBalance() {
return accountBalance;
}
public void setaccountBalance(double accountBalance) {
this.accountBalance=accountBalance;
}
public boolean getaccountStatus() {
return accountStatus;
}
public void setaccountStatus(boolean accountStatus) {
this.accountStatus=accountStatus;
}
public double getaccountDeposit() {
return accountDeposit;
}
public void setaccountDeposit(double accountDeposit) {
this.accountDeposit=accountDeposit;
}
public double getaccountWithdrawel() {
return accountWithdrawel;
}
public void setaccountWithdrawel(double accountWithdrawel) {
this.accountWithdrawel=accountWithdrawel;
}
}